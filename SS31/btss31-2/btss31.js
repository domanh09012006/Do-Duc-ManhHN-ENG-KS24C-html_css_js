document.getElementById("btn-dark").onclick = function () {
    if (document.getElementById("txt-change").style.backgroundColor = "white") {
        document.getElementById("txt-change").style.backgroundColor = "black"
        document.getElementById("txt-change").style.color = "white"
    }else if(document.getElementById("txt-change").style.backgroundColor = "black"){
        document.getElementById("txt-change").style.backgroundColor = "white"
        document.getElementById("txt-change").style.color = "black"
    }

}